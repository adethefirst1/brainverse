  /// Picks [count] unique words for [category], constrained by [minLen] and [maxLen].
  /// Falls back to the 'General' category if [category] is not found.
  static List<String> pickWords({
    required String category,
    required int count,
    required int minLen,
    required int maxLen,
  }) {
    final rng = Random();

    final baseWords =
        wordsByCategory[category] ?? wordsByCategory['General'] ?? const <String>[];

    // Sanitize: uppercase and strip non A-Z.
    final sanitized = baseWords
        .map((w) => w.toUpperCase().replaceAll(RegExp(r'[^A-Z]'), ''))
        .where((w) => w.isNotEmpty)
        .toSet()
        .toList();

    int low = minLen;
    int high = maxLen;

    List<String> _filterByLength(int minL, int maxL) {
      return sanitized.where((w) => w.length >= minL && w.length <= maxL).toList();
    }

    // Try progressively relaxed ranges: exact, +/-2, +/-4.
    final ranges = <(int, int)>[
      (low, high),
      (max(1, low - 2), high + 2),
      (max(1, low - 4), high + 4),
    ];

    List<String> candidates = [];
    for (final (minL, maxL2) in ranges) {
      candidates = _filterByLength(minL, maxL2);
      if (candidates.length >= count || maxL2 >= 20) {
        break;
      }
    }

    if (candidates.isEmpty) {
      // If nothing matches even with relaxed bounds, fall back to any sanitized word.
      candidates = List.of(sanitized);
    }

    candidates.shuffle(rng);
    if (candidates.length > count) {
      return candidates.sublist(0, count);
    }
    return candidates;
  }
